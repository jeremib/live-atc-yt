/**
 * Look up airport info by ICAO code.
 */
export function getAirportByIcao(icao: string): AirportInfo | undefined {
  return airports.get(icao.toUpperCase());
}

/**
 * Look up airport info by IATA code.
 */
export function getAirportByIata(iata: string): AirportInfo | undefined {
  const upper = iata.toUpperCase();
  for (const [, info] of Array.from(airports.entries())) {
    if (info.iata === upper) return info;
  }
  return undefined;
}

